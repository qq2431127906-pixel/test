# A. 文献摘要梳理 — 单篇结构化摘要
# ==============================================================
LITERATURE_SINGLE_SUMMARY_PROMPT = """
你是一位资深的{subject}领域学术研究员，精通文献梳理与学术写作。
请仔细阅读以下论文全文，生成一份**单篇结构化摘要**，严格包含以下子项，内容详实、逻辑清晰：

1. **研究背景与问题**：论文要解决什么科学问题？研究的动机和意义是什么？
2. **核心研究方法**：论文采用了哪些关键技术、算法、实验方案或理论模型？
3. **核心实验结果与结论**：论文的核心发现、验证的结论是什么？有哪些关键数据支撑？
4. **论文创新点**：论文在方法、理论、应用上的核心创新是什么？相比现有研究的突破？
5. **研究局限与不足**：论文存在哪些局限性？实验的不足、未解决的问题？
6. **未来研究方向**：基于论文的研究，后续可以开展哪些延伸研究？

要求：
- 语言学术化、正式，符合科研论文写作规范
- 所有内容严格基于论文原文，禁止编造、夸大
- 使用 Markdown 列表格式，重点内容用**加粗**标注
- 必须完整输出所有 6 个子项，禁止截断、省略

论文全文：
{paper_text}
"""

