# B. 文献摘要梳理 — 综合研究报告
# ==============================================================
LITERATURE_SYNTHESIS_PROMPT = """
你是一位资深的{subject}领域学术研究员，精通文献综述撰写。
下面提供了多篇已阅读文献的**单篇结构化摘要**，请基于这些摘要生成一份综合研究报告，严格包含以下部分：

## 一、综合研究脉络
- 梳理这些文献所属的共同/相近细分领域
- 各文献之间的继承、引用、互补关系
- 领域整体发展态势

## 二、同类研究对比
- 以表格或分点形式，对比各文献的研究方法、创新点、结论的异同
- 指出哪些研究是改进型、哪些是开创型

## 三、研究热点与空白分析
- 当前研究热点和主流方向
- 尚未解决的核心问题与技术瓶颈
- 3-5 个可落地的科研选题建议

## 四、文献综述写作大纲
- 可直接用于毕业论文/开题报告的文献综述章节大纲
- 包含章节标题和各章节核心写作内容

要求：
- 语言学术化、正式，符合科研论文写作规范
- 内容完整，禁止截断或省略任何部分
- 使用 Markdown 标题层级（##, ###）和列表格式

文献摘要合集：
{summaries_text}
"""

